// Helper to generate smooth river path with more points
function interpolateRiverPath(waypoints: [number, number][], pointsPerSegment: number = 15): [number, number][] {
  const result: [number, number][] = [];

  for (let i = 0; i < waypoints.length - 1; i++) {
    const current = waypoints[i];
    const next = waypoints[i + 1];
    if (!current || !next) continue;

    const [lat1, lng1] = current;
    const [lat2, lng2] = next;

    for (let j = 0; j < pointsPerSegment; j++) {
      const t = j / pointsPerSegment;
      const meander = Math.sin(t * Math.PI * 4) * 0.015;
      const lat = lat1 + (lat2 - lat1) * t + meander * (Math.random() - 0.5);
      const lng = lng1 + (lng2 - lng1) * t + meander * (Math.random() - 0.5) * 1.2;
      result.push([Number(lat.toFixed(4)), Number(lng.toFixed(4))]);
    }
  }
  const lastPoint = waypoints[waypoints.length - 1];
  if (lastPoint) {
    result.push(lastPoint);
  }
  return result;
}

// Helper to generate detailed lake/bay shoreline
function generateDetailedShoreline(roughOutline: [number, number][], detailLevel: number = 12): [number, number][] {
  const result: [number, number][] = [];

  for (let i = 0; i < roughOutline.length; i++) {
    const current = roughOutline[i];
    const next = roughOutline[(i + 1) % roughOutline.length];
    if (!current || !next) continue;

    const [lat1, lng1] = current;
    const [lat2, lng2] = next;

    for (let j = 0; j < detailLevel; j++) {
      const t = j / detailLevel;
      const irregularity = Math.sin(t * Math.PI * 6 + i) * 0.02 + Math.sin(t * Math.PI * 2) * 0.01;
      const lat = lat1 + (lat2 - lat1) * t + irregularity * (0.5 - Math.random());
      const lng = lng1 + (lng2 - lng1) * t + irregularity * (0.5 - Math.random()) * 1.2;
      result.push([Number(lat.toFixed(4)), Number(lng.toFixed(4))]);
    }
  }

  return result;
}

