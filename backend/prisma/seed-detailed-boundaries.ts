async function updateDetailedBoundaries(): Promise<void> {
  console.log("Starting detailed boundary coordinate update...\n");

  const updates: { name: string; coords: [number, number][] }[] = [
    // Rivers
    { name: "St. Lawrence River", coords: stLawrenceRiverCoords },
    { name: "Mackenzie River", coords: mackenzieRiverCoords },
    { name: "Fraser River", coords: fraserRiverCoords },
    { name: "Nelson River", coords: nelsonRiverCoords },
    { name: "Hayes River", coords: hayesRiverCoords },
    { name: "Churchill River (Saskatchewan)", coords: churchillRiverCoords },
    { name: "Saskatchewan River", coords: saskatchewanRiverCoords },
    { name: "Ottawa River", coords: ottawaRiverCoords },
    { name: "Red River", coords: redRiverCoords },
    { name: "Assiniboine River", coords: assiniboineRiverCoords },
    // Lakes
    { name: "Lake Superior", coords: lakeSuperiorCoords },
    { name: "Lake Huron", coords: lakeHuronCoords },
    { name: "Lake Ontario", coords: lakeOntarioCoords },
    { name: "Lake Winnipeg", coords: lakeWinnipegCoords },
    { name: "Great Slave Lake", coords: greatSlaveLakeCoords },
    { name: "Great Bear Lake", coords: greatBearLakeCoords },
    { name: "Lake Athabasca", coords: lakeAthabascaCoords },
    // Bays
    { name: "Hudson Bay", coords: hudsonBayCoords },
    { name: "James Bay", coords: jamesBayCoords },
    { name: "Georgian Bay", coords: georgianBayCoords },
  ];

  let successCount = 0;
  let notFoundCount = 0;

  for (const update of updates) {
    try {
      // First check if the waterway exists
      const existing = await prisma.waterway.findFirst({
        where: { name: update.name }
      });

      if (existing) {
        await prisma.waterway.update({
          where: { id: existing.id },
          data: { boundaryCoordinates: JSON.stringify(update.coords) }
        });
        console.log(`  Updated: ${update.name} (${update.coords.length} points)`);
        successCount++;
      } else {
        console.log(`  Not found: ${update.name} - waterway does not exist in database`);
        notFoundCount++;
      }
    } catch (error) {
      console.error(`  Error updating ${update.name}:`, error);
    }
  }

  console.log("\n========================================");
  console.log("Detailed Boundary Update Complete");
  console.log("========================================");
  console.log(`Successfully updated: ${successCount} waterways`);
  console.log(`Not found: ${notFoundCount} waterways`);
  console.log("\nCoordinate counts:");

  for (const update of updates) {
    console.log(`  - ${update.name}: ${update.coords.length} points`);
  }
}

// Main execution
async function main(): Promise<void> {
  try {
    await updateDetailedBoundaries();
  } catch (error) {
    console.error("Error running seed:", error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

main()
  .catch((e) => {
    console.error("Fatal error:", e);
    process.exit(1);
  });
